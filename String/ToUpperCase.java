package string;

//In String the toUpperCase() method use to convert LowerCase String value to UpperCase String value.

public class ToUpperCase

{

	public static void main(String[] args)

	{
		String Name = "aashik";

		System.out.println("(aashik) convert to (AASHIK) :" + Name.toUpperCase());// toUpperCase() method.
	}

}